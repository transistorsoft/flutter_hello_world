void headlessTask(bg.HeadlessEvent headlessEvent) async {
  print('*** [BackgroundGeolocation HeadlessTask]: $headlessEvent');
}

void main() {
  runApp(MyApp());
  bg.BackgroundGeolocation.registerHeadlessTask(headlessTask);
}

